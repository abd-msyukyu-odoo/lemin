#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	size_t		l1;
	size_t		i2;

	l1 = ft_strlen(s1);
	i2 = -1;
	while (s2[++i2] != '\0')
		s1[l1 + i2] = s2[i2];
	s1[l1 + i2] = '\0';
	return (s1);
}
