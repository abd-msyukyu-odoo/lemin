char	*ft_strcat(char *s1, char const *s2)
{
	int		i;
	int		pos;

	pos = 0;
	while (s1[pos])
		pos++;
	i = 0;
	while (s2[i])
	{
		s1[pos + i] = s2[i];
		i++;
	}
	s1[pos + i] = '\0';
	return (s1);
}
