#include "libft.h"

void		ft_putnbr2_fd(long n, int fd)
{
	if (n < 10 && n > -10)
	{
		if (n < 0)
			ft_putchar_fd('-', fd);
	}
	else
		ft_putnbr2_fd(n / 10, fd);
	ft_putchar_fd(((n < 0) ? -1 : 1) * (n % 10) + '0', fd);
}
